/*
  Reverse a linked list
  The input list will have at least one element  
  Return the node which points to the head of the new LinkedList
  Node is defined as 
    struct node
    {
        int data;
        struct node* next;
    
        node(int x){
            data = x;
            next = NULL;
        }
    
    }*head;
*/

class Solution
{
    public:
    struct node *reverse (struct node *head, int k)
    { 
        if(head==NULL or head->next==NULL or k==1)return head;
        struct node *s=head,*e=head;
        int inc =k-1;
        while(inc--){
            e=e->next;
            if(e->next==NULL) break;
        }
        struct node *nexthead=reverse(e->next,k);
        reverseen(s,e);
        s->next=nexthead;
        return e;
        
    }
    
    void reverseen(struct node *s,struct node *e){
        struct node *p=NULL,*c=s,*n=s->next;
        while(p!=e){
            c->next=p;
            p=c;
            c=n;
            if(n!=NULL)n=n->next;
        }
    }
};


